const propertyTypes = [
  'House',
  'Townhouses',
  'Condos',
  'Duplexes',
  'Studios',
  'Villas',
  'Apartments',
  'Others',
];
const facilities = [
  'Laundry',
  'CarParking',
  'SportsCenter',
  'Cutlery',
  'Gym',
  'SwimmingPool',
  'Wifi',
  'PetCenter',
];

async function main() {
  try {
    // Seed Agents
    const agents = await Promise.all(
      [1, 2, 3, 4, 5].map(async num => {
        return await prisma.agents.create({
          data: {
            name: `Agent ${num}`,
            email: `agent${num}@example.com`,
            avatar: agentImages[Math.floor(Math.random() * agentImages.length)],
          },
        });
      }),
    );

    // Seed Galleries
    const galleries = await Promise.all(
      galleryImages.map(async image => {
        return await prisma.galleries.create({
          data: {image},
        });
      }),
    );

    // Seed Properties
    for (let i = 1; i <= 20; i++) {
      const agent = agents[Math.floor(Math.random() * agents.length)];

      const property = await prisma.properties.create({
        data: {
          name: `Property ${i}`,
          type: propertyTypes[Math.floor(Math.random() * propertyTypes.length)],
          description: `Luxury property located in prime location. Property number ${i}`,
          address: `${i} Main Street, City`,
          price: Math.floor(Math.random() * 900000) + 100000,
          area: Math.floor(Math.random() * 3000) + 500,
          bedrooms: Math.floor(Math.random() * 5) + 1,
          bathrooms: Math.floor(Math.random() * 3) + 1,
          rating: Math.random() * 4 + 1,
          facilities: facilities.slice(
            0,
            Math.floor(Math.random() * facilities.length),
          ),
          image: propertiesImages[i] || propertiesImages[0],
          geolocation: `${Math.random()},${Math.random()}`,
          agentId: agent.id,
          gallery: {
            connect: galleries.slice(0, 3).map(g => ({id: g.id})),
          },
        },
      });

      // Add Reviews for each property
      await Promise.all(
        [1, 2, 3].map(async num => {
          return await prisma.reviews.create({
            data: {
              name: `Reviewer ${num}`,
              avatar:
                reviewImages[Math.floor(Math.random() * reviewImages.length)],
              review: `Great property! Review ${num}`,
              rating: Math.floor(Math.random() * 5) + 1,
              propertyId: property.id,
            },
          });
        }),
      );
    }
  } catch (error) {
    console.error(error);
    process.exit(1);
  } finally {
    await prisma.$disconnect();
  }
}

main();
